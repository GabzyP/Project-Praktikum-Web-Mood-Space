function renderLocalMusic(tracks) {
    const container = document.getElementById('local-music-container');
    if (!tracks || tracks.length === 0) {
        container.innerHTML = `<div style="text-align:center; padding: 2rem; opacity: 0.6;"><p>Belum ada koleksi lagu tambahan.</p></div>`;
        return;
    }
    let htmlContent = `<div class="music-grid">`;
    tracks.forEach((track, index) => {
        htmlContent += `
            <div class="music-card">
                <div class="music-cover-wrapper">
                    <img src="${track.cover}" class="music-cover" id="cover-${index}" alt="${track.title}">
                    <button class="custom-play-btn" onclick="playLocalMusic('${track.file}', 'cover-${index}', this)">
                        <i class="fas fa-play"></i>
                    </button>
                </div>
                <div class="music-info"><h3>${track.title}</h3><p>${track.artist}</p></div>
            </div>`;
    });
    htmlContent += `</div>`;
    container.innerHTML = htmlContent;
}

function playLocalMusic(file, coverId, btn) {
    const icon = btn.querySelector('i');
    const cover = document.getElementById(coverId);
    
    if (currentAudio && currentAudio.src.includes(file) && !currentAudio.paused) {
        currentAudio.pause();
        icon.className = 'fas fa-play'; 
        cover.classList.remove('playing');
        return;
    }
    
    if (currentAudio) {
        currentAudio.pause();
        currentAudio.currentTime = 0;
        document.querySelectorAll('.custom-play-btn i').forEach(i => i.className = 'fas fa-play');
        document.querySelectorAll('.music-cover').forEach(c => c.classList.remove('playing'));
    }
    
    currentAudio = new Audio(file);
    currentAudio.play();
    icon.className = 'fas fa-pause'; 
    cover.classList.add('playing');
    
    currentAudio.onended = () => {
        icon.className = 'fas fa-play';
        cover.classList.remove('playing');
    };
}


function renderVideoPage(data) {
    const container = document.getElementById('dynamic-video-container');
    const allVideos = data.videos || [];

    if (allVideos.length === 0) {
        container.innerHTML = "<p class='text-center'>Belum ada video.</p>";
        return;
    }

    const mainVideo = allVideos[0];
    let videoSrc = mainVideo.src || "https://www.w3schools.com/html/mov_bbb.mp4"; 
    let sideListHTML = '';
    for (let i = 1; i < allVideos.length; i++) {
        const vid = allVideos[i];
        let thumbDisplay = vid.thumb 
            ? `<img src="${vid.thumb}" alt="${vid.title}">`
            : `<video src="${vid.src}#t=1.0" preload="metadata" muted style="width:100%; height:100%; object-fit:cover;"></video>`;

        sideListHTML += `
            <div class="side-card" onclick="changeMainVideo('${vid.src}', '${vid.title}', '${vid.channel || 'Shorts'}')">
                <div class="side-thumb">
                    ${thumbDisplay}
                </div>
                <div class="side-info">
                    <h4>${vid.title}</h4>
                    <p>${vid.channel || 'MoodSpace Playlist'}</p>
                </div>
            </div>
        `;
    }

    container.innerHTML = `
        <div class="cinema-grid">
            <div class="main-player-wrapper">
                <div class="main-video-frame">
                    <video id="mainVideoPlayer" controls autoplay poster="${mainVideo.thumb || ''}">
                        <source src="${videoSrc}" type="video/mp4">
                        Browser tidak support.
                    </video>
                </div>
                <div class="main-info">
                    <h2 id="mainVideoTitle">${mainVideo.title}</h2>
                    <p id="mainVideoChannel">${mainVideo.channel || 'MoodSpace Channel'}</p>
                </div>
            </div>

            <div class="side-list">
                ${sideListHTML}
            </div>
        </div>
    `;
}

function changeMainVideo(src, title, channel) {
    const player = document.getElementById('mainVideoPlayer');
    const titleEl = document.getElementById('mainVideoTitle');
    const channelEl = document.getElementById('mainVideoChannel');

    player.src = src;
    player.play(); 
    titleEl.innerText = title;
    channelEl.innerText = channel;
    
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

