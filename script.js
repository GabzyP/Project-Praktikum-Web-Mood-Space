function addNewQuote() {
    const quoteInput = document.getElementById('newQuote');
    const authorInput = document.getElementById('newAuthor');
    const grid = document.getElementById('quotes-grid-target');

    if (!quoteInput || quoteInput.value.trim() === "") {
        alert("Tulis quote-nya dulu dong! üòä");
        return;
    }

    const newCard = document.createElement('div');
    newCard.className = 'quote-card-box';
    newCard.style.animation = "fadeIn 0.5s";  
    newCard.innerHTML = `
        <p class="quote-text">"${quoteInput.value}"</p>
        <span class="quote-author">‚Äî ${authorInput.value || 'Anonim'}</span>
    `;
    
    if (grid.children.length > 1) {
        grid.insertBefore(newCard, grid.children[1]);
    } else {
        grid.appendChild(newCard);
    }

    quoteInput.value = "";
    authorInput.value = "";
}

